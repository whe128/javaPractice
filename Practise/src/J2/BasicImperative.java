package J2;

public class BasicImperative {
    public static void main(String[] args) {
        int i = 0;
        System.out.println("i: "+i);
        i = i + 7;
        System.out.println("i: "+i);
        i = 256;
        System.out.println("i: "+i);
        i = i % 10;
        System.out.println("i: "+i);
    }
}
