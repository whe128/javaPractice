package J3;

public class Integers {
    public static void main(String[] args) {

        //start with unicode,_,$
        int $x = 6;
        System.out.println($x);

        int y = 0x10;
        System.out.println(y);

        int z = 0b110010;
        System.out.println(z);

        int s = Integer.parseInt("11");
        System.out.println(s);

    }
}
